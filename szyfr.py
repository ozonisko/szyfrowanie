def szyfrujObraz(kierunek):
    for obraz in lb.get(0, lb.size()):
        img = Image.open(obraz)
        img = img.convert("RGBA")
        pixdata = img.load()
        i = 0
        if kierunek == 1:
            for y in range(img.size[1]):
                for x in range(img.size[0]):  # int('{:08b}'.format(n)[::-1], 2)
                    pixdata[x, y] = (
                        int('{:08b}'.format(pixdata[x, y][0] ^ kod[i])[::-1], 2),
                        int('{:08b}'.format(pixdata[x, y][1] ^ kod[i])[::-1], 2),
                        int('{:08b}'.format(pixdata[x, y][2] ^ kod[i])[::-1], 2),
                        pixdata[x, y][3])
                    if x == 0 and y == 0:
                        print(pixdata[x, y])
                    i = (i + 1) % len(kod)
            img.save(obraz[:-4] + "_crypted" + ".png")
        else:
            for y in range(img.size[1]):
                for x in range(img.size[0]):  # int('{:08b}'.format(n)[::-1], 2)
                    pixdata[x, y] = (
                        int('{:08b}'.format(pixdata[x, y][0])[::-1], 2) ^ kod[i],
                        int('{:08b}'.format(pixdata[x, y][1])[::-1], 2) ^ kod[i],
                        int('{:08b}'.format(pixdata[x, y][2])[::-1], 2) ^ kod[i],
                        pixdata[x, y][3])
                    #if x == 0 and y == 0:
                    #    print(pixdata[x, y])
                    i = (i + 1) % len(kod)
            if obraz[-12:-4] == "_crypted":
                img.save(obraz[:-12] + "_decrypted" + ".jpg", "JPEG")
            else:
                img.save(obraz[:-4] + "_decrypted" + ".png")
        lb.delete(0, END)
        messagebox.showinfo(" ", "Zakonczono!")


def szyfrujTekst(kierunek):
    for file in lb.get(0, END):
        txt = open(file, "r", encoding="utf-8")
        tekst = txt.read()
        txt.close()
        i = 0
        new = ""
        out = None
        for znak in range(0, len(tekst)):
            new += chr(ord(tekst[znak]) ^ kod[i])
            i = (i + 1) % len(kod)
        if kierunek == 1:
            out = open(file[:-4] + "_crypted" + ".txt", "w", encoding="utf-8")
        else:
            if file[-12:-4] == "_crypted":
                out = open(file[:-12] + "_decrypted" + ".txt", "w", encoding="utf-8")
            else:
                out = open(file[:-4] + "_decrypted" + ".txt", "w", encoding="utf-8")
        out.write(new)
        out.close()
        lb.delete(0, END)
        messagebox.showinfo(" ", "Zakonczono!")


